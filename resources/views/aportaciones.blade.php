                      <p class="card-text">La Astronomía y sus procesos de investigación dieron lugar en 2019 al descubrimiento de un agujero negro supermasivo, ubicado en el centro de la galaxia Messier 87, que se encuentra a cincuenta y cinco millones de años luz de la Tierra <br> <br>

                        Este hallazgo fue posible gracias al trabajo de doscientos científicos/as que recurrieron a telescopios profesionales de todo el mundo, para captar la imagen esperada. <br> <br>
                        
                        La importancia de esta noticia radica en que, a partir de ese momento, la ciencia de la Astrofísica va a poder seguir estudiando en profundidad la Teoría de la Relatividad de Einstein, que, a principios del siglo XX, daba valor a la potencia de la gravedad de los supuestos agujeros negros, (hoy día ya confirmados en su existencia, a raíz de este hallazgo), por la cual se produce la distorsión del espacio-tiempo. <br> <br>
                        
                        Entre las vías de estudio que seguirá la Astrofísica, durante los próximos años, se continuará trabajando para lograr la primera imagen de "Sagitario A": el agujero negro que se encuentra en el centro de nuestra Vía Láctea, y del cual aún en 2020 no se ha podido obtener ninguna imagen. <br> <br>
                        
                        El hallazgo del primer agujero negro supermasivo, de igual manera, da rienda suelta a las nuevas teorías que la Astrofísica estudiará sobre la necesidad de que este tipo de fenómenos existan para la concepción de la vida, tal y como la conocemos.</p>
